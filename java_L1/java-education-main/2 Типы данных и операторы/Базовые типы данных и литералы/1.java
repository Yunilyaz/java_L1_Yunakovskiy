//Приведены примеры всех типов данны (в том числе и нулевые)
public class DataTypes {
    public static void main(String args[]) {
	//Целочисленные
        byte b0 = 0;
        byte b1 = 8;

        short s0 = 0;
        short s1 = 64;

        int i0 = 0;
        int i1 = 1024;

        long l0 = 0;
        long l1 = 1234567890;

	//С плавающей точкой
        float f0 = 0.0f;
        float f1 = 4.4f;

        double d0 = 0.0;
        double d1 = 0.3d;

	//Символьный
        char c0 = 0;
        char c1 = 'a';

	//Булевский
        boolean bl0 = false;
        boolean bl1 = true;
    }
}